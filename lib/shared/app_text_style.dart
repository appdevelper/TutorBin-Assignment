import 'package:flutter/cupertino.dart';
import 'package:flutter/material.dart';

regularTextStyle({required double fontSize, Color? color}) {
  return TextStyle(
      fontSize: fontSize, color: color ?? Colors.black);
}

mediumTextStyle({required double fontSize, Color? color}) {
  return TextStyle(
      fontSize: fontSize, color: color ?? Colors.black);
}

semiBoldTextStyle({required double fontSize, Color? color}) {
  return TextStyle(
      fontSize: fontSize, color: color ?? Colors.black);
}

boldTextStyle({required double fontSize, Color? color}) {
  return TextStyle(
      fontSize: fontSize, color: color ?? Colors.black);
}
